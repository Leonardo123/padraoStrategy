package CodigoStrategy;

public class Funcionario {

	private String rg, tipoDeCaixa,tipoDeVendedor, cpf, profissao, codigoDaEmpresa, setor, nºDoCaixa;

	
	public String getRg() {
		return rg;
	}

	public void setRg(String rg) {
		this.rg = rg;
	}

	public String getTipoDeVendedor() {
		return tipoDeVendedor;
	}

	public void setTipoDeVendedor(String tipoDeVendedor) {
		this.tipoDeVendedor = tipoDeVendedor;
	}

	public String getTipoDeCaixa() {
		return tipoDeCaixa;
	}

	public void setTipoDeCaixa(String tipoDeCaixa) {
		this.tipoDeCaixa = tipoDeCaixa;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getProfissao() {
		return profissao;
	}

	public void setProfissao(String profissao) {
		this.profissao = profissao;
	}

	public String getCodigoDaEmpresa() {
		return codigoDaEmpresa;
	}

	public void setCodigoDaEmpresa(String codigoDaEmpresa) {
		this.codigoDaEmpresa = codigoDaEmpresa;
	}

	public String getSetor() {
		return setor;
	}

	public void setSetor(String setor) {
		this.setor = setor;
	}

	public String getNºDoCaixa() {
		return nºDoCaixa;
	}

	public void setNºDoCaixa(String nºDoCaixa) {
		this.nºDoCaixa = nºDoCaixa;
	}
	
}
